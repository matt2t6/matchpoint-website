// Spatial Awareness Module - SVG-based tracker layout
const SpatialAwareness = ({ clusters, selectedCluster, onClusterSelect }) => {
  const [hoveredCluster, setHoveredCluster] = useState(null);
  
  const clusterPositions = useMemo(() => {
    // Generate grid positions for clusters
    const cols = 4;
    const rows = 2;
    const positions = [];
    
    for (let i = 0; i < clusters.length; i++) {
      const row = Math.floor(i / cols);
      const col = i % cols;
      positions.push({
        id: i + 1,
        x: col * 60 + 30,
        y: row * 60 + 30,
        status: clusters[i]?.status || 'optimal'
      });
    }
    return positions;
  }, [clusters]);

  return (
    <Card className="relative overflow-hidden">
      <CardHeader icon={MapPin} subtitle="Real-time Spatial Layout" status="active">
        Spatial Awareness
      </CardHeader>
      <CardContent>
        <div className="relative w-full h-64 bg-slate-900 rounded-lg border border-slate-700">
          <svg className="w-full h-full" viewBox="0 0 240 120">
            {/* Grid background */}
            <defs>
              <pattern id="grid" width="20" height="20" patternUnits="userSpaceOnUse">
                <path d="M 20 0 L 0 0 0 20" fill="none" stroke="#334155" strokeWidth="0.5"/>
              </pattern>
            </defs>
            <rect width="100%" height="100%" fill="url(#grid)" />
            
            {/* Clusters */}
            {clusterPositions.map((cluster) => (
              <g key={cluster.id}>
                <circle
                  cx={cluster.x}
                  cy={cluster.y}
                  r={cluster.id === selectedCluster ? 8 : 6}
                  className={`transition-all duration-300 cursor-pointer ${
                    cluster.id === selectedCluster 
                      ? 'fill-cyan-400 stroke-cyan-300 stroke-2' 
                      : cluster.id === hoveredCluster
                      ? 'fill-slate-400 stroke-slate-300 stroke-1'
                      : `fill-${cluster.status === 'optimal' ? 'green' : cluster.status === 'warning' ? 'yellow' : 'red'}-400`
                  }`}
                  onMouseEnter={() => setHoveredCluster(cluster.id)}
                  onMouseLeave={() => setHoveredCluster(null)}
                  onClick={() => onClusterSelect(cluster.id)}
                />
                <text
                  x={cluster.x}
                  y={cluster.y + 4}
                  textAnchor="middle"
                  className="text-xs fill-slate-300 font-mono"
                >
                  {cluster.id}
                </text>
              </g>
            ))}
            
            {/* Connection lines for FPS clusters */}
            {clusterPositions.map((cluster, i) => {
              if (i % 5 === 0 && i + 4 < clusterPositions.length) {
                const clusterGroup = clusterPositions.slice(i, i + 5);
                return clusterGroup.map((c, j) => {
                  if (j < clusterGroup.length - 1) {
                    const next = clusterGroup[j + 1];
                    return (
                      <line
                        key={`${c.id}-${next.id}`}
                        x1={c.x}
                        y1={c.y}
                        x2={next.x}
                        y2={next.y}
                        stroke="#22d3ee"
                        strokeWidth="1"
                        strokeDasharray="2,2"
                        opacity="0.6"
                      />
                    );
                  }
                  return null;
                });
              }
              return null;
            })}
          </svg>
          
          {/* Status overlay */}
          {hoveredCluster && (
            <div className="absolute top-2 right-2 bg-slate-800/90 p-2 rounded text-xs">
              <div className="text-cyan-400 font-semibold">Cluster {hoveredCluster}</div>
              <div className="text-slate-400">
                Status: {clusters[hoveredCluster - 1]?.status || 'unknown'}
              </div>
            </div>
          )}
        </div>
      </CardContent>
    </Card>
  );
};

